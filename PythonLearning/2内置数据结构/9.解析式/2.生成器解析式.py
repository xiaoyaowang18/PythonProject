'''
列表解析中的中括号变成小括号，就称为生成器解析式了。
生成器解析式返回的是一个生成器
优点：1.不占内存 2.惰性求值
'''
g = ((x * 2 for x in range(500000)))
print(type(g))

# 获取值
v1 = next(g)
print(v1)
v2 = next(g)
print(v2)

# 按需读取，只要一个值，就给我一个值，不要一下子都丢过来，内存会溢出


# 生成器都是迭代器，生成器无法用下标访问的。

'''
结论：
    明确知道要用下标访问的时候，用列表解析，只需要对结果迭代的时候，优先使用生成器解析
    
    如果要用缓存的话，就不能用生成器解析了，要用列表解析
'''