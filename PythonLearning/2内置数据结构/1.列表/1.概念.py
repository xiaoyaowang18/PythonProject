'''
列表是一种数据结构
列表是一个序列，用于顺序存储数据
序列是python中最基本的数据结构
序列中的每个元素都分配一个数字：它的位置或索引，第一个索引是0，第二个索引是1，以此类推。
'''

# 使用list()函数定义空列表
l = list()

# 使用中括号定义空列表
l = []

# 使用中括号定义带初始值的列表
l = [1, 2, 3]

# 使用list函数把可迭代对象转换为列表
l = list(range(0, 10))

# 通常在定义列表的时候用 [] ，在转换可迭代对象为列表时用list函数。
