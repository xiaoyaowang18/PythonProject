def add(x,y):
    print(x,y)
    return x+y

'''
def表示定义一个函数，紧接着是函数名，函数名后面用一对小括号列出函数列表，参数列表后面使用一个冒号开始函数体

函数体是正常的python语句，可以包含任何结构，包括函数

return表示函数的返回值

函数有输入参数和输出返回值，函数其实是一个代码单元。把输入转化为输出。
'''


