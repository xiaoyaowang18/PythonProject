'''
1、定义

一个变量的可见范围叫做这个变量的作用域

接触函数前，所有的变量都作用在一个范围里，所有没有作用域的概念，现在学了函数就有了，因为python是一个非块级作用域的语言。

所有的块级作用域就是tab或者空格表示一个代码块的。

为什么在pycharm编辑器里缩进都用tab，但是到服务器调试代码时，用tab发现不对，只能用4个空格呢？
因为编辑器里事先做了优化，把4个空格等效于一个tab
'''

for i in range(10):
    name = i

print(i) # 9
print(name) # 9

# 发现在循环之后，name和i变量还可以被继续使用。
# 因为python是一个非块级作用域的语言，以为上面4行代码,其实都是在一个级别下的。


