def add(x, y):
    ret = x + y
    print('{}+{}'.format(x, y))
    return ret


print(add(x=2, y=3))

# 2.关键字参数
'''
如果参数太多，记不住顺序，怎么办？
可以通过关键参数的方式传递参数，这样就不用遵守参数的顺序了。
这种通过指定关键字传递，参数按照定义时的变量名传递，叫做关键字参数
'''

